import React, { useEffect, useState } from "react";
import { View, Text, FlatList, TouchableOpacity, ActivityIndicator, StyleSheet } from "react-native";
import axios from "axios";
import { useRouter } from "expo-router";
import JobDetails from "./job-details";


type Job = {
  id: number;
  title: string;
  company_name: string;
  description: string;
  url: string;
};


export default function HomeScreen() {
  // const [jobs, setJobs] = useState([]);        // stores fetched jobs
  const [jobs, setJobs] = useState<Job[]>([]);
  const [loading, setLoading] = useState(true); // tracks loading state
  const router = useRouter();                   // navigation handler

  useEffect(() => {
    fetchJobs();
  }, []);

  const fetchJobs = async () => {
    try {
      const response = await axios.get("https://remotive.com/api/remote-jobs");
      setJobs(response.data.jobs.slice(0, 20)); // take first 20 for demo
    } catch (error) {
      console.log("Error fetching jobs:", error);
    } finally {
      setLoading(false);
    }
  };

  if (loading) {
    return <ActivityIndicator size="large" style={{ marginTop: 50 }} />;
  }

  return (
    <View style={styles.container}>
      <Text style={styles.title}>Remote Jobs</Text>

      <FlatList
        data={jobs}
        keyExtractor={(item) => item.id.toString()}
        renderItem={({ item }) => (
          <TouchableOpacity
            style={styles.card}
            onPress={() => 
              router.push({ pathname: "/job-details", params: { job: JSON.stringify(item) } })}
          >
            <Text style={styles.jobTitle}>{item.title}</Text>
            <Text style={styles.company}>{item.company_name}</Text>
          </TouchableOpacity>
        )}
      />
    </View>
  );
}

const styles = StyleSheet.create({
  container: { flex: 1, backgroundColor: "#fff", padding: 20 },
  title: { fontSize: 24, fontWeight: "bold", marginBottom: 10 },
  card: {
    backgroundColor: "#f9f9f9",
    padding: 15,
    borderRadius: 10,
    marginBottom: 10,
    shadowColor: "#000",
    shadowOpacity: 0.1,
    shadowRadius: 4,
    elevation: 2,
  },
  jobTitle: { fontSize: 16, fontWeight: "bold" },
  company: { color: "gray", marginTop: 3 },
});
